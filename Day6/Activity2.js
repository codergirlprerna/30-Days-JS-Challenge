//Array methods:
//task-3 push ka use karke array k end me ek naya number add karo aur updated array ko console par log karo
const numbers = [1,2,3,4,5];
numbers.push(7);
console.log(numbers);

//task-4 pop ka use karke array k end se ek number pop kro aur updated array console par log karo
numbers.pop();
console.log(numbers);

//task-5 shift ka use krke array k start se number delete karo aur updated array console par log karo
numbers.shift();
console.log(numbers);

//task-6 unshift method ka use karke array ke starting me ek naya number add karo or updated array console par log karo
numbers.unshift(8);
console.log(numbers);